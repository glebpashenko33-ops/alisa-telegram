// Возвращает активный промпт: из БД (если есть) или дефолтный из кода.
// При ошибке БД — тихий fallback, бот не падает.
async function getActivePrompt() {
  try {
    const fromDb = await db.getPrompt();
    if (fromDb) return fromDb;
    // Первый запуск — сохраняем дефолтный в БД
    await db.setPrompt(SYSTEM_PROMPT);
    return SYSTEM_PROMPT;
  } catch (e) {
    console.error('[prompt] Ошибка получения промпта из БД, используем дефолтный:', e.message);
    return SYSTEM_PROMPT;
  }
}

